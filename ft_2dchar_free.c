#include "libft.h"

int		ft_2dchar_free(char ***map)
{
	int x;

	if (!map || *map)
		return (0);
	x = 0;
	while (*map[x])
	{
		free(*map[x]);
		x++;
	}
	free(*map);
	*map = NULL;
	return (0);
}
